CREATE OR REPLACE TRIGGER trg_check_birthdate
BEFORE INSERT OR UPDATE ON employee
FOR EACH ROW
BEGIN
	IF :NEW.birth_date > add_months(:NEW.hire_date, -216) THEN -- 216 = 12 * 18
		RAISE_APPLICATION_ERROR(-20001, 'The employee must be at least 18 years old on the hire date.');
	END IF;
END;

