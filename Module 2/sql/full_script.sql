/*
	--------------	   ------------
	| DEPARTMENT |  -> | EMPLOYEE |
	--------------     ------------
	      |					  |
	      v					  v
	-----------			------------
	| PROJECT |			| TIMECARD |
	-----------			------------
	      |					  |
	      |					  |	
	      -------      --------
	      	    |      |
	      	    v      v
  	       ------------------	  --------------
  	       | TIMECARD_ENTRY | ->  | WORK_ENTRY |
  	       ------------------	  --------------
        As there is a cycle TIMECARD_ENTRY - TIMECARD - EMPLOYEE - DEPARTMENT - PROJECT
        we have to prevent the following unexpected behaviour:
      ** An user inserts a project_code into one of his timecard entries, but that project code does not
      * exist in the user's department
      * so we get the department id of the project in the new timecard entry
      * and the department id of the user
      * and make sure they are the same before inserting or updating
*/
CREATE OR REPLACE TRIGGER trg_check_project_department
BEFORE INSERT OR UPDATE ON timecard_entry
FOR EACH ROW
DECLARE
v_project_department_id NUMBER;
v_employee_department_id NUMBER;
BEGIN
	-- project department
	SELECT p.department_id 
	INTO v_project_department_id
	FROM project p
	WHERE p.project_id = :NEW.project_id;

	-- employee department
	SELECT e.department_id
	INTO v_employee_department_id
	FROM timecard t
	JOIN employee e ON t.employee_id = e.person_number
	WHERE t.id = :NEW.timecard_id;

	IF (v_project_department_id <> v_employee_department_id) THEN
		RAISE_APPLICATION_ERROR(-20002, 'Cannot insert a timecard entry with a project that is not from your department.');
	END IF;
END;
/

