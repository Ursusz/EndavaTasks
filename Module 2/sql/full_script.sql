		'TIMECARD',
		'PROJECT',
		'EMPLOYEE',
		'DEPARTMENT',
		'TASK'
	);
BEGIN	
	FOR i IN 1..v_table_names.count LOOP
		EXECUTE IMMEDIATE 'DROP TABLE ' || v_table_names(i) || ' CASCADE CONSTRAINTS';
	END LOOP;
	EXCEPTION WHEN OTHERS THEN 
		IF SQLCODE != -942 THEN
			RAISE;
		END IF;
END;
/

